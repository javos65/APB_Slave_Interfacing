// draw text string, 4bbp font, capitals only
void draw_text( const char *text, font4bpp myfont, uint8_t x, uint8_t y,uint16_t color1)
{
char c=0;
uint16_t wcomp,bytew,width,ip,cc,height = myfont.height;
uint8_t  *fmap= (uint8_t*) myfont.fontmap;
uint16_t *findex = (uint16_t*) myfont.fontindex;
uint16_t charcursor=0;

cc=0;
//printf("Printing [%s]\n\r",text);
while( (c=text[cc]) !=0 ){
 if(c>myfont.last) c=c-32; // change small to large ascii
 if ( (c>=myfont.first) && (c<=myfont.last)){
      ip =  *( (uint16_t*) findex + (c-myfont.first)*2 + 1);         // pointer to first pixel
      width= *( (uint16_t*) findex + (c-myfont.first)*2 + 0);        // number of pixels wide
      //printf(" *Char [%c] w*h=%d*%d",c,width,height);
      if (width%2) {width+=1; bytew=width/2; wcomp=1;} // odd pixels, stored 1x 4bpp more, compensate cursor move.
      else { bytew=width/2;wcomp=0;}
      //printf(" * Character: %c points to index %d\n\r",c,ip);
      draw_font( (uint8_t *) (fmap + ip),(uint8_t) x+charcursor,(uint8_t) y,(uint8_t) width, (uint8_t) height, color1) ;
       charcursor+= (width-wcomp);
  }
 cc++; if(cc>36) break; // escape in case there is no ending 0
 }
}

// draw one letter of font - using Global font_buffer
void draw_font(uint8_t *pSrc, uint8_t x, uint8_t y,uint8_t xSize, uint8_t ySize,uint16_t color)
{
	uint8_t fpixel,b_counter=0;
    uint16_t rr,gg,bb,rrr,ggg,bbb;
    bb=(uint16_t) (color & 0x001f); gg=(uint16_t)  ((color>>5)& 0x003f); rr=(uint16_t)  ((color>>11)& 0x001f);
    //printf("xS,yS [%d][%d] Color[0x%04x]\n",xSize,ySize,color);
    //printf("RGB [0x%04x][0x%04x][0x%04x]\n",rr,gg,bb);
	for (uint8_t r=0;r<ySize;r++)
		for( uint8_t c=0;c<xSize/2;c++) // 4fpixel, counts half the bytes
		{
			//compute color code1 - fpixel lower 4 bits
			fpixel = *(pSrc+ c + (r*xSize/2));fpixel= fpixel & 0x0f;
			bbb= ((  (uint16_t) bb*fpixel)/0x000f)      & 0x001f;
			ggg= ((( (uint16_t) gg*fpixel)/0x000f)<< 5) & 0x07e0;
			rrr= ((( (uint16_t) rr*fpixel)/0x000f)<<11) & 0xf800;
		    G_font_buffer[b_counter++]=rrr+ggg+bbb;
			//printf("pixel[%d] [0x%02x][0x%04x][0x%04x][0x%04x]=[0x%04x]]\n",c,fpixel,rrr,ggg,bbb,rgb565);

			//compute color code2 - fpixel higher 4 bits
			fpixel = *(pSrc+ c + (r*xSize/2));fpixel= (fpixel>>4) & 0x0f;
			bbb= ((  (uint16_t) bb*fpixel)/0x000f)      & 0x001f;
			ggg= ((( (uint16_t) gg*fpixel)/0x000f)<< 5) & 0x07e0;
			rrr= ((( (uint16_t) rr*fpixel)/0x000f)<<11) & 0xf800;
		    G_font_buffer[b_counter++]=rrr+ggg+bbb;
		}
	// Send font_Buffer as a block
	command_ssd1351(SSD1351_CMD_SETCOLUMN);	data_ssd1351(x);data_ssd1351(x+xSize-1);
	command_ssd1351(SSD1351_CMD_SETROW);	data_ssd1351(y);data_ssd1351(y+ySize-1);
	command_ssd1351(SSD1351_CMD_WRITERAM);
	bbb=b_counter; // use bbb as temp register to count back in reverse
	while(b_counter !=0){
		data_ssd1351((uint8_t) (G_font_buffer[bbb-b_counter-1]>>8) );
		data_ssd1351((uint8_t) (G_font_buffer[bbb-b_counter-1]) );
		b_counter--;
		}
}

